-- TABLE OUTPUT
SELECT * FROM WORKER;

-- 1. MAX, MIN

SELECT MAX(SALARY) AS MAX_S, MIN(SALARY) AS MIN_S
FROM WORKER

-- 2. REVERSE ORDER

SELECT REVERSE (FIRST_NAME) AS REVERSE_FIRSTNAME FROM WORKER

-- 3. JOINING YEAR

SELECT FIRST_NAME AS F_NAME, YEAR(JOINING_DATE) AS JOINING_YEAR
FROM WORKER

-- 4. DATEDIFF

SELECT FIRST_NAME AS F_NAME, DATEDIFF(DAY, JOINING_DATE, GETDATE()) AS TOTAL_DAYS
FROM WORKER

-- 5. ADD PROBATION

SELECT *, DATEADD(MONTH, 6, JOINING_DATE) AS PROBATION_END FROM WORKER

-- 6. COUNT

SELECT DEPARTMENT, COUNT(DEPARTMENT) AS TOTAL_WORKER
FROM WORKER
GROUP BY DEPARTMENT

-- 7.

SELECT FIRST_NAME AS F_NAME, LAST_NAME AS L_NAME
FROM WORKER

